#[cfg(test)]
mod tests {
    use super::*;
    use stabstream_dem::DetectorErrorModel;

    const REPETITION_DEM: &str = "error(0.1) D0 D1 ^ L0\n\
                                   error(0.1) D1 D2\n\
                                   error(0.1) D2 ^ L0\n\
                                   detector D0\n\
                                   detector D1\n\
                                   detector D2\n\
                                   logical_observable L0\n";

    #[test]
    fn mean_dem_probability_correct() {
        let dem = DetectorErrorModel::parse(REPETITION_DEM).unwrap();
        let p = mean_dem_probability(&dem);
        assert!((p - 0.1).abs() < 1e-12);
    }

    #[test]
    fn flat_shot_to_window_active_detectors() {
        let events = vec![true, false, true];
        let w = flat_shot_to_window(&events, 3);
        let active = w.active_detectors();
        assert!(active.contains(&0));
        assert!(!active.contains(&1));
        assert!(active.contains(&2));
    }

    #[test]
    fn run_null_decoder_zero_errors() {
        // With the null decoder, p_l should be 0 (no corrections predicted,
        // and observable_flips from sampler is also 0 when DEM has no L terms).
        let dem =
            DetectorErrorModel::parse("error(0.1) D0 D1\ndetector D0\ndetector D1\n").unwrap();
        let sampler = Arc::new(DemSampler::from_dem(&dem));
        let acc = Arc::new(LogicalErrorAccumulator::new(1));
        let acc_ref = Arc::clone(&acc);
        let n = 1000u64;
        (0..n)
            .into_par_iter()
            .map_init(
                || SmallRng::from_entropy(),
                |rng, _| {
                    let shot = sampler.sample(rng);
                    let result = DecoderResult::empty();
                    // ground truth also 0 since no L edges in DEM
                    acc_ref.record(&result, shot.observable_flips);
                },
            )
            .for_each(|()| {});
        assert_eq!(acc.total_shots(), n);
        // No L edges → ground_truth always 0, decoder always says 0 → 0 errors
        assert!((acc.logical_error_rate(0)).abs() < f64::EPSILON);
    }
}
