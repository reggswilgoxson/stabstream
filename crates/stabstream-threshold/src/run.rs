/// Wrap a flat shot's detector events in a single-round SyndromeWindow so the
/// UF decoder can consume them.  Node id i = detector i (no spacetime round
/// multiplier needed — the DEM already encodes spacetime structure).
fn flat_shot_to_window(detector_events: &[bool], ancilla_count: usize) -> SyndromeWindow {
    let mut window = SyndromeWindow::new(ancilla_count, 1);
    window.push_owned(OwnedSyndromeData {
        frame_id: 0,
        round: 0,
        timestamp_ns: 0,
        detector_events: detector_events.to_vec(),
        meas_results: vec![],
    });
    window
}

/// Infer p_physical from a DEM as the mean error mechanism probability.
fn mean_dem_probability(dem: &DetectorErrorModel) -> f64 {
    if dem.errors.is_empty() {
        return 0.0;
    }
    dem.errors.iter().map(|e| e.probability).sum::<f64>() / dem.errors.len() as f64
}

